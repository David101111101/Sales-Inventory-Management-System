									<p class="p1"><a href="#"><H1>Proceso productivo</H1></a></p><BR>
									<BR>
									<BR>
									
								<div><img  height="250" align="right" src="images/procesoProductivo1.jpg" width="160" /></div>
								
							 <strong><h5>Preparación de Pasta:</h5></strong><br><br>
							 La primera etapa del proceso productivo es la preparación de la mezcla.
							 Las materias primas, debidamente escogidas, son llevadas al hidrapulper donde el papel reciclado es mezclado con agua y <br>
							 desintegrado por la acción de un rotor de cuchillas. En esta etapa los operarios prestan contralan con mucha presición el tiempo<br>
							 la proporción de agua y fibra y las dosificaciones de los químicos para lograr la calidad de la pasta que garantizará el éxito<br>
							 del resto de la producción
							 <br><br>
								<div><img  align="center" src="images/mezcla.jpg"  /></div><br>
								<strong><h5>Refino o Limpieza:</h5></strong>
								Al salir del hidrapulper, la pasta es sometida a un proceso de refino, que consiste en pasarla a través de un conjunto de 
								tanques y varios sistemas de limpieza, para atrapar las “gomas”, esto es, los lomos de los directorios o plásticos grandes 
								incorporados en las otras materias primas. Estas gomas son rechazadas del proceso.La pasta contiene además elementos pesados 
								como ganchos de cosedora, alambres, partículas de arena y otros que son retirados en un Limpiador de Alta Densidad y 
								las particulas más pequeñas restantes se extraen con el equipo Fiber Sorter.La pasta, limpia y debidamente desintegrada,
								está lista para pasar a una de nuestras seis máquinas moldeadoras HARTMANN.Los rechazos de estos procesos de limpieza son 
								almacenados para ser dispuestos debidamente en un relleno sanitario. <br><br>
								
								
								<strong><h5>Moldeo:</h5></strong>
								La pasta se entrega a cada máquina moldeadora. Allí, la pasta depositada en los moldes es sometida a varios procesos
								de succión y presión, que le dan la forma y disminuyen la cantidad de agua en el producto. En el mismo molde de presión 
								el producto  para al proceso de secado.
								
								<strong><h5>Secado:</h5></strong>
								El secador es una caja dentro de la cual un sistema de quemadores de gas y ventiladores de aire inyectan aire caliente 
								a 200°C para calentar el agua presente en las bandejas y retirarla en forma de vapor. Las bandejas recorren el secador a 
								lo largo una y otra vez, hasta que, pasados unos minutos, salen de él para ser entregadas al sistema de apilado, que consiste
								de unos transportadores de banda, un mecanismo de volteo, otro de compresión y otro de flejado para poder convertir las
								bandejas individuales en paquetes de bandejas (en grupos de 100 o 120) y de esta forma poderlas almacenar, transportar y
								utilizar adecuadamente en las avícolas.

								
